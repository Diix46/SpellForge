export function useLocale() {
  // SSR-safe shared singleton; lazily hydrated from localStorage on the client.
  const locale = useState<Locale>('locale', loadLocale)

  function setLocale(l: Locale) {
    locale.value = l
    if (import.meta.client)
      localStorage.setItem(STORAGE_KEY, l)
  }

  function toggle() {
    setLocale(locale.value === 'fr' ? 'en' : 'fr')
  }

  /** Translate a key. Falls back to the key itself if missing. */
  function t(key: string): string {
    return messages[locale.value][key] ?? messages.fr[key] ?? key
  }

  function rarityLabel(rarity?: string): string {
    if (!rarity)
      return ''
    return t(`rarity.${rarity.toLowerCase()}`) || rarity
  }

  const isFr = computed(() => locale.value === 'fr')

  // Short locale-aware date (e.g. "22 juin" / "Jun 22") for deck timestamps.
  // Shared by the dashboard and deck tiles so the format stays in one place.
  function formatShortDate(ts: number): string {
    return new Date(ts).toLocaleDateString(locale.value === 'fr' ? 'fr-FR' : 'en-US', { day: '2-digit', month: 'short' })
  }

  return { locale, setLocale, toggle, t, rarityLabel, isFr, formatShortDate }
}
